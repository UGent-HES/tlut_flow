public class ConeEnumeration implements Visitor<Node, Edge> {
	
	private static final int maxConeSizeConsidered = GlobalConstants.maxConeSizeConsidered;
	private static final int maxBddSizeConsidered = GlobalConstants.maxBddSizeConsidered;
	private static final int maxBddSizeConsideredToMerge = GlobalConstants.maxBddSizeConsideredToMerge;
	private static final int maxNumConesPerNodeConsidered = GlobalConstants.maxNumConesPerNodeConsidered;
	private static final int maxNumConesPerNodeSaved = GlobalConstants.maxNumConesPerNodeSaved;

	protected int K;
	protected boolean tcon_mapping_flag;
	protected boolean tlc_mapping_flag;
	protected boolean build_bdd_function;
	protected int nmbrConsideredCones;
	protected int nmbrFeasibleCones;
	protected int nmbrDominatedCones;
	protected int nmbrCones;
	protected BDDidMapping bddIdMapping;

	public ConeEnumeration(int K, boolean tcon_mapping_flag, boolean tlc_mapping_flag) {
		nmbrConsideredCones = 0;
		nmbrDominatedCones = 0;
		nmbrCones = 0;
		this.K = K;
		this.tcon_mapping_flag = tcon_mapping_flag;
		this.tlc_mapping_flag = tlc_mapping_flag;
		this.build_bdd_function = tcon_mapping_flag || tlc_mapping_flag;
	}
	
	public int getNmbrConsideredCones() {
		return nmbrConsideredCones;
	}
	
	public int getNmbrFeasibleCones() {
		return nmbrFeasibleCones;
	}
	
	public int getNmbrDominatedCones() {
		return nmbrDominatedCones;
	}

	public int getNmbrCones() {
		return nmbrCones;
	}

	public void init(AIG<Node, Edge> aig) {
		if(!(aig instanceof MappingAIG))
			throw new RuntimeException("ConeEnumeration should be constructed with MappingAIG object");
		bddIdMapping = ((MappingAIG)aig).getBDDidMapping();
	}

	public void visit(Node node) {
		ConeSet result = new ConeSet(node);

		if (node.isPrimaryInput()) {
			result.add(Cone.trivialCone(node, bddIdMapping, build_bdd_function));
			
		} else if(node.isGate()) {
			if (node.isParameter())
				result.addAll(mergeParameterConeSets(node));
			else {
				ConeSet cones = mergeInputConeSets(node);
				cones = retainFeasibleCones(cones);
				nmbrFeasibleCones += cones.size();
				cones = removeDominatedCones(cones);
				//cones = customFilterCones(cones);
				cones = limitNumCones(cones);
				nmbrCones += cones.size();
				result.addAll(cones);
				if(nodeNeedsTrivialCone(node, result))
					result.add(Cone.trivialCone(node, bddIdMapping, build_bdd_function));
			}
			
		} else if(node.isPrimaryOutput()) {
			result.add(Cone.outputCone(node, bddIdMapping, build_bdd_function));
		} else {
		}
		result.reduceMemoryUsage();
		node.setConeSet(result);
		node.setConesEnumerated();
		
		if(GlobalConstants.freeBDDafterEnumeration) {
			for(Edge e : node.getInputEdges()) {
				Node parent = e.getTail();
				if(parent.isFanoutConeEnumerationDone()) {
					parent.getConeSet().free();
				}
			}
		}
		
		// System.out.println(node.getName() + ": " + nmbrCones);
	}

	@Override
	public void finish(AIG<Node,Edge> aig) {}
	
	protected boolean nodeNeedsTrivialCone(Node node, ConeSet coneset) {
		if(!node.isGate())
			throw new RuntimeException("This function expects a AND node");
		
		//if node has a primary output as parent, this node will be the root of a selected cone anyway
		for(Edge edge : node.getOutputEdges()) {
			if(edge.getHead().isPrimaryOutput())
				return true;
		}
		
		if (node.isParameter())
			return false;
		if (node.getI0().getTail().isParameter() ||
		 		node.getI1().getTail().isParameter() )
		 	return false;
		
		
		//if TLCs are enabled, every TCON cone can be merged with a TLUT or TCON cone at the output of the node
		if(tlc_mapping_flag) {
			for(Cone cone : coneset.getCones()) {
				if(cone.isTCON())
					return false;
			}
//			for(Cone cone : coneset.getCones()) {
//				if(!cone.isTCON())
//					return true;
//			}
//			return false;
		}
		return true;
	}

	protected ConeSet mergeParameterConeSets(Node node) {
		// Get the two child nodes of the current node
		Node node0 = node.getI0().getTail();
		Node node1 = node.getI1().getTail();

		Cone merge = Cone.mergeCones(node, node0.getConeSet().getOnlyCone(), node1.getConeSet().getOnlyCone(), 
				tcon_mapping_flag ? maxConeSizeConsidered : K,
				maxBddSizeConsidered, build_bdd_function);
		if(merge == null) 
			throw new RuntimeException("Parameter coneset should contain one cone");
		merge.mapToNone();
		
		ConeSet result = new ConeSet(node);
		result.add(merge);
		return result;
	}

	protected ConeSet mergeInputConeSets(Node node) {
		// Get the two child nodes of the current node
		Node node0 = node.getI0().getTail();
		Node node1 = node.getI1().getTail();

		// Get the cone sets of the child nodes
		ConeSet coneSet0 = node0.getConeSet();
		ConeSet coneSet1 = node1.getConeSet();

		// Merge the cone sets of the child nodes
		class TwoCones {
			public Cone cone0, cone1;
			public TwoCones(Cone cone0, Cone cone1) {
				this.cone0 = cone0;
				this.cone1 = cone1;
			}
		}
		ArrayList<TwoCones> mergesToConsider = new ArrayList<TwoCones>();

		boolean coneSkipped = false;

		// Filter cones that are not considered to merge
		ArrayList<Cone> cones0 = new ArrayList<Cone>();
		ArrayList<Cone> cones1 = new ArrayList<Cone>();
		for(Cone c : coneSet0.getCones()) {
			if(considerToMergeCone(c))
				cones0.add(c);
			else {
				coneSkipped = true;
				Logger.getLogger().log(new ConeNotConsideredToMerge(c));
			}
		}
		for(Cone c : coneSet1.getCones()) {
			if(considerToMergeCone(c))
				cones1.add(c);
			else {
				coneSkipped = true;
				Logger.getLogger().log(new ConeNotConsideredToMerge(c));
			}
		}
		
		Collections.sort(cones0, new SizeConeComparator());
		Collections.sort(cones1, new SizeConeComparator());
//		Collections.sort(cones0, new BDDSizeConeComparator());
//		Collections.sort(cones1, new BDDSizeConeComparator());
		
		
		if(cones0.size() > cones1.size()) {
			ArrayList<Cone> tmp = cones0;
			cones0 = cones1;
			cones1 = tmp;
		}
		
		EnumerateConesToConsider1 : for(int i=0; i<cones0.size(); i++) {
			for(int j=0; j<=i; j++) {
				mergesToConsider.add(new TwoCones(cones0.get(i), cones1.get(j)));
				if(i!=j)
					mergesToConsider.add(new TwoCones(cones0.get(j), cones1.get(i)));
				if(mergesToConsider.size() >= maxNumConesPerNodeConsidered)
					break EnumerateConesToConsider1;
			}
		}
		EnumerateConesToConsider2 : for(int i=cones0.size(); i<cones1.size(); i++) {
			for(Cone cone0 : cones0) {
				mergesToConsider.add(new TwoCones(cone0, cones1.get(i)));
				if(mergesToConsider.size() >= maxNumConesPerNodeConsidered)
					break EnumerateConesToConsider2;
			}
		}
		nmbrConsideredCones += mergesToConsider.size();
		if(mergesToConsider.size() >= maxNumConesPerNodeConsidered)
			Logger.getLogger().log(new ConeNumToConsiderReached(node));

		ConeSet result = new ConeSet(node);
		for(TwoCones twoCones : mergesToConsider) {
			Cone merge = Cone.mergeCones(node, twoCones.cone0, twoCones.cone1, 
					tcon_mapping_flag ? maxConeSizeConsidered : K,
					maxBddSizeConsidered, tcon_mapping_flag);
			if(merge != null) {
				result.add(merge);
				if(GlobalConstants.enableStatsFlag)
					Logger.getLogger().log(new ConeConsideredStats(merge));
			} else {
				coneSkipped = true;
			}
		}
		
		/*
		 * If two cones are not merged (because of heuristic optimisation) then
		 * add the cone containing the two inputs of the node In most cases this
		 * cone is already produced by merging the corresponding trivial cones,
		 * but in TCONMAP it is possible that this doesn't happen because the
		 * trivial cone of one of the inputs is not available (due to heuristic
		 * optimisation)
		 */
		if(coneSkipped)
			result.add(Cone.twoInputCone(node, bddIdMapping, build_bdd_function));
		return result;
	}
	
	private boolean considerToMergeCone(Cone cone) {
		if(cone.getFunction() == null)
			return true;
		return cone.getFunction().nodeCount() <= maxBddSizeConsideredToMerge;
	}
	
	protected ConeSet limitNumCones(ConeSet cones) {
		if(cones.size() <= maxNumConesPerNodeSaved)
			return cones;
		
		ConeSet result = new ConeSet(cones.getNode());

		ArrayList<Cone> temp = new ArrayList<Cone>(cones.getCones());
		Collections.sort(temp, new SizeConeComparator());
		
		result.addAll(temp.subList(0, maxNumConesPerNodeSaved-1));
		
		for(Cone cone : temp.subList(maxNumConesPerNodeSaved, temp.size()))
			cone.free();

		if(cones.getCones().size() > maxNumConesPerNodeSaved)
			Logger.getLogger().log(new ConeNumToSaveReached(cones.getNode()));
		
		return result;
	}

	
	protected ConeSet removeDominatedCones(ConeSet feasibleCones) {
		ConeSet result = new ConeSet(feasibleCones.getNode());
		result.addAll(feasibleCones);

		Set<Cone> dominatedCones = new HashSet<Cone>();
		ArrayList<Cone> temp = new ArrayList<Cone>(result.getCones());
		Collections.sort(temp, new SizeConeComparator());

		for (int i = 0; i < temp.size(); i++) {
			if (temp.get(i) == null)
				continue;
			for (int j = i + 1; j < temp.size(); j++) {
				if (temp.get(j) == null)
					continue;
				if (temp.get(i).dominates(temp.get(j))) {
					dominatedCones.add(temp.get(j));
					temp.set(j, null);
				}
			}
		}

		result.removeAll(dominatedCones);
		nmbrDominatedCones += dominatedCones.size();

		for(Cone c : dominatedCones)
			c.free();
		
		return result;
	}
	
	protected ConeSet retainFeasibleCones(ConeSet mergedConeSet) {
		ConeSet feasibleCones = new ConeSet(mergedConeSet.getNode());
		for (Cone c : mergedConeSet) {
			c.initFeasibilityCalculation(this.tcon_mapping_flag);
			boolean feasible = true;
			if(c.isUnmapped()) {
				if(this.tcon_mapping_flag && c.isTCONfeasible()) {
					c.mapToTCON();
				} else if(c.isTLUTfeasible(K)) {
					if(c.isLUTfeasible(K))
						c.mapToLUT();
					else
						c.mapToTLUT();
				} else if(this.tlc_mapping_flag && c.isTLCfeasible(K)) {
					c.mapToTLC();
				} else {// infeasible
					feasible = false;
				}
			}
			if(GlobalConstants.enableStatsFlag)
			Logger.getLogger().log(new ConeFeasibilityStats(c));
			if(feasible) {
				feasibleCones.add(c);
			} else {
				c.free();
			}
			c.finishFeasibilityCalculation();
		}
		return feasibleCones;
	}

}
