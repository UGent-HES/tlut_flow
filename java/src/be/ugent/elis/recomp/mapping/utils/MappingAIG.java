    private class ConfigurationEntry {
        String name;
        Node copy;
        boolean copyInv;
        ConfigurationEntry(String n, Node c0, boolean c1) {name = n; copy = c0; copyInv = c1;}
    }

	public AIG<Node, Edge> constructParamConfig(int K, boolean includeTLUTs, boolean includeLUTs) {
		AIG<Node, Edge> aig = new MappingAIG();
		

		Map<Node, PolarisedNode<Node>> parameterCopyMap = new HashMap<Node, PolarisedNode<Node>>();
		for (Node input:this.getInputs()) {
			if (input.isParameter()) {
				Node copy = aig.addNode(input.getName(), NodeType.INPUT);
				parameterCopyMap.put(input, new PolarisedNode<Node>(copy, false));
			}
		}
		
		for (Node and : getAnds()) {
			if (and.isVisible()) {
			    if(!( (includeTLUTs &&  and.getBestCone().isTuneable()) || 
			          (includeLUTs  && !and.getBestCone().isTLUT())    ))
    			          continue;
				
				Cone bestCone = and.getBestCone();
				ArrayList<Node> bestConeNodesInToOut = bestCone.getNodesInToOut();
				ArrayList<Node> regularInputs = new ArrayList<Node>(bestCone.getRegularLeaves());
				
				ArrayList<ConfigurationEntry> configuration_entries = new ArrayList<ConfigurationEntry>();
				ArrayList<ConfigurationEntry> configuration_entries_inv = new ArrayList<ConfigurationEntry>();
								
				for (int entry=0; entry < Math.pow(2, bestCone.size()); entry++) {
					Map<Node,PolarisedNode<Node>> copyMap = new HashMap<Node,PolarisedNode<Node>>(parameterCopyMap);

					int temp = entry;
					for (Node input : regularInputs) {
						copyMap.put(input, new PolarisedNode<Node>(aig.getConst0(), 
							(temp % 2 != 0) ^
							(checkOutputLutInversion(input) == OutputLutInversion.AllOutsInverted)));
						temp = temp / 2;
					}					
					
					for (Node orig: bestConeNodesInToOut) {
						Node origI0 = orig.getI0().getTail();
						PolarisedNode<Node> pnodeI0 = new PolarisedNode<Node>(copyMap.get(origI0));
						pnodeI0.toggleInverted(orig.getI0().isInverted());
						
						Node origI1 = orig.getI1().getTail();
						PolarisedNode<Node> pnodeI1 = new PolarisedNode<Node>(copyMap.get(origI1));
						pnodeI1.toggleInverted(orig.getI1().isInverted());
						
//						Constant propagation
						if (pnodeI1.getNode() == aig.getConst0()) {
							PolarisedNode<Node> swap_var = pnodeI1;
							pnodeI1 = pnodeI0;
							pnodeI0 = swap_var;
						}
						if (pnodeI0.getNode() == aig.getConst0()) {
							if (pnodeI0.isInverted()) {
								copyMap.put(orig, pnodeI1);
							} else {
								copyMap.put(orig, new PolarisedNode(aig.getConst0(), false));
							}
//						No constant propagation possible
						} else {
							Node copy = aig.findNode(pnodeI0.getNode(), pnodeI0.isInverted(), pnodeI1.getNode(), pnodeI1.isInverted());
							if (copy == null) {
								copy = aig.addNode(and.getName()+"_"+entry+"_"+orig.getName(), pnodeI0.getNode(), pnodeI0.isInverted(), pnodeI1.getNode(), pnodeI1.isInverted());
							}
							copyMap.put(orig, new PolarisedNode(copy, false));
						}
					}
							
					if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted || 
					        checkOutputLutInversion(and) == OutputLutInversion.MixedOuts) {
						PolarisedNode<Node> pnode = copyMap.get(and);
						configuration_entries_inv.add(new ConfigurationEntry(and.getName()+"not"+"_"+entry, 
																pnode.getNode(), !pnode.isInverted()));
					}
					if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted) {
						PolarisedNode<Node> pnode = copyMap.get(and);
						configuration_entries.add(new ConfigurationEntry(and.getName()+"_"+entry, 
																pnode.getNode(), pnode.isInverted()));
					}
				}
				
				configuration_entries_inv.addAll(configuration_entries);
				for(ConfigurationEntry entry : configuration_entries_inv) {
                    Node output = aig.addNode(entry.name, NodeType.OUTPUT);
                    Edge e = aig.addEdge(entry.copy, output, entry.copyInv);
                    output.setI0(e);
                    entry.copy.addOutput(e);
                }
			}
		}
		aig.sortInputsAlphanumerically();
		aig.sanityCheck();
		return aig;
	}

		
	
	
	public enum OutputLutInversion{
		notAnOutputLut, AllOutsNotInverted, AllOutsInverted, MixedOuts
	}
	
	// Determine the OutputLutInversion of an output Lut
	public OutputLutInversion checkOutputLutInversion(Node node) {
	    if(!node.isGate())
			return OutputLutInversion.notAnOutputLut;
	    
		// Determine whether it is a node and connected with an output
		boolean isOutput = false;
		Edge firstEdge = null;
		for(Edge e : node.getOutputEdges()) {
			Node head = e.getHead();
			if (head.isOutput() || head.isILatch()) {
				isOutput = true;
				firstEdge = e;
				break;
			}
		}
		if(!isOutput) {
			return OutputLutInversion.notAnOutputLut; 
		} else {
			// Determine the outputLutInversion
			OutputLutInversion outlutInv;
			if(firstEdge.isInverted())
			    outlutInv = OutputLutInversion.AllOutsInverted;
			else
			    outlutInv = OutputLutInversion.AllOutsNotInverted;
			for(Edge e : node.getOutputEdges()) {
				Node head = e.getHead();
				if (head.isOutput() || head.isILatch()) {
					if (e.isInverted() && outlutInv == OutputLutInversion.AllOutsNotInverted || 
					        (!e.isInverted() && outlutInv == OutputLutInversion.AllOutsInverted)) {
						outlutInv = OutputLutInversion.MixedOuts;
						break;
					}
				}
			}
			return outlutInv;
		}
	}
	
	public MappedCircuit constructMappedCircuit(String name, int K) {
		MappedCircuit circuit = new MappedCircuit(name);

		HashMap<PolarisedNode<Node>,MappedNode> mapping = new HashMap<PolarisedNode<Node>,MappedNode>();
		HashMap<PolarisedNode<Node>,MappedPrimaryOutput> outputMapping = new HashMap<PolarisedNode<Node>,MappedPrimaryOutput>();
		BDDidMapping<MappedNode> nBddIdMapping = new BDDidMapping<MappedNode>(); 

		mapping.put(new PolarisedNode<Node>(getConst0(), false), circuit.getConst0());
		for(Node in : getInputs()) {
			MappedInput mappedN = circuit.addInput(in.getName());
			mapping.put(new PolarisedNode<Node>(in, false), mappedN);
			nBddIdMapping.mapNodeToId(mappedN, bddIdMapping.getId(in));
		}
		for(Node out : getOutputs()) {
			MappedOutput mappedN = circuit.addOutput(out.getName());
			outputMapping.put(new PolarisedNode<Node>(out, false), mappedN);
		}
		for(Node olatch : getOLatches()) {
			Node latch = olatch.getI0().getTail();
			Node ilatch = latch.getI0().getTail();
			MappedLatch mappedN = circuit.addLatch(latch.getName());
			mapping.put(new PolarisedNode<Node>(olatch, false), mappedN);
			nBddIdMapping.mapNodeToId(mappedN, bddIdMapping.getId(olatch));
			outputMapping.put(new PolarisedNode<Node>(ilatch, false), mappedN);
		}
		
		for(Node and : topologicalOrderInToOut(false, false)) {
			if (and.isGate() && and.isVisible()) {
				BooleanFunction<MappedNode> function = new BooleanFunction<MappedNode>(nBddIdMapping, and.getBestCone().getLocalFunction());
				MappedGate mappedN = circuit.addGate(and.getName(), function.getInputVariables(), function, and.getBestCone().getType().toString());
				mapping.put(new PolarisedNode<Node>(and, false), mappedN);
				nBddIdMapping.mapNodeToId(mappedN, bddIdMapping.getId(and));

//				if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted
//				        || checkOutputLutInversion(and) == OutputLutInversion.MixedOuts) {
//					printLutVhdl(baseName, and, stream, nameStream, K, and.getName()+"not", true);	 
//				}
//				if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted) {
//					printLutVhdl(baseName, and, stream, nameStream, K, and.getName(), false);
//				}
			}
		}
		
		for(Node output : getAllPrimaryOutputs()) {
			MappedPrimaryOutput mappedO = outputMapping.get(new PolarisedNode<Node>(output, false));
			MappedNode mappedS = mapping.get(new PolarisedNode<Node>(output.getI0().getTail(), false));
			MappedNode mappedS_inv = mapping.get(new PolarisedNode<Node>(output.getI0().getTail(), true));
			if(mappedO == null)
				throw new RuntimeException();
			if(mappedS == null)
				throw new RuntimeException();
			if(output.getI0().isInverted()) {
				if(mappedS_inv == null) {
					BDDidMapping<MappedNode> newBddIdMapping = new BDDidMapping<MappedNode>();
					newBddIdMapping.mapNodeToId(mappedS, 1);
					BDD newBdd = BDDFactorySingleton.get().nithVar(1);
					BooleanFunction<MappedNode> newBooleanFunction = new BooleanFunction<MappedNode>(newBddIdMapping, newBdd);
					mappedS_inv = circuit.addGate(mappedS.getName()+"_not", newBooleanFunction.getInputVariables(), newBooleanFunction, "LUT");
					mapping.put(new PolarisedNode<Node>(output.getI0().getTail(), true), mappedS_inv);
				}
				mappedO.setSource(mappedS_inv);
			} else {
				mappedO.setSource(mappedS);
			}
		}
		
		return circuit;
	}
	


	public void printLutStructureBlif(PrintStream stream, int K) {
		stream.println(".model top");
		
		stream.print(".inputs");
		//Regular inputs
		for (Node in : getInputs()) {
			if (!in.isParameter()) {
				stream.print(" "+in.getName());
			}
		}
		//TLUT configuration inputs
		for (Node and : getAnds()) {
			if (and.isVisible() && and.getBestCone().isTuneable()) {
				if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted 
						|| checkOutputLutInversion(and) == OutputLutInversion.MixedOuts) {
					for (int entry=0; entry < Math.pow(2,and.getBestCone().size()); entry++) {
						stream.print(" "+and.getName()+"not"+"_"+entry);
					}
				}
				if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted) {
					for (int entry=0; entry < Math.pow(2,and.getBestCone().size()); entry++) {
						stream.print(" "+and.getName()+"_"+entry);
					}
				}
			}
		}
		stream.println();
	
		//Regular outputs
		stream.print(".outputs");		
		for (Node out : getOutputs()) {
			stream.print(" "+out.getName());
		}
		stream.println();
		stream.println();
		
		//Constant 0
		stream.println(".names "+ getConst0().getName());
		stream.println("0");
		stream.println();
				
		//Latches
		for (Node latch : getLatches()) {
			printLatchBlif(latch, stream);
		}
		stream.println();
		
		//LUTs and TLUTs
		for (Node and : getAnds()) {
			if (and.isVisible()) {
				if(and.getBestCone().isTuneable()) {
					if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted 
							|| checkOutputLutInversion(and) == OutputLutInversion.MixedOuts)
						printTLutBlif(and, stream, and.getBestCone().size(), and.getName()+"not");
					if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted)
						printTLutBlif(and, stream, and.getBestCone().size(), and.getName());
				} else {
					if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted 
							|| checkOutputLutInversion(and) == OutputLutInversion.MixedOuts)
						printLutBlif(and, stream, and.getName()+"not", true);
					if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted)
						printLutBlif(and, stream, and.getName(), false);
				}
			}
		}
		
		//Output inversion (conditional)
		//The BLIF format is a little annoying.
		for (AbstractNode<Node, Edge> out : getOutputs()) {
			Edge e = out.getI0();
			Node node = e.getTail();
			if(checkOutputLutInversion(node) == OutputLutInversion.AllOutsInverted 
					|| (checkOutputLutInversion(node) == OutputLutInversion.MixedOuts && e.isInverted())) {
				stream.println(".names "+node.getName()+"not"+" "+out.getName());
				stream.println("1 1");
			} else {
				if (!node.getName().equals(out.getName())) {
					stream.println(".names "+node.getName()+" "+out.getName());
					if (e.isInverted()) {
						stream.println("0 1");
					} else {
						stream.println("1 1");
					}
				} else {
					// In some cases a latch has the same name as an output
					// this should solve that problem.
					if (e.isInverted())
			            throw new RuntimeException("Unexpected inversion of edge");
				}
			}	
		}
		
		stream.print(".end");	
		
		stream.flush();
	}

	public void printLutBlif(Node and, PrintStream stream, String lutName, boolean inverted) {
		Cone bestCone = and.getBestCone();
		BooleanFunction<Node> f = bestCone.getBooleanFunction(false);
		if(inverted) {
			BooleanFunction<Node> f_old = f;
			f = f_old.invert();
			f_old.free();
		}
		
		stream.print(".names");
		//Regular LUT inputs
		for(Node n : f.getInputVariables()) {
			if (n != null &&
					checkOutputLutInversion(n) == OutputLutInversion.AllOutsInverted) {
				stream.print(" "+n.getName()+"not");
				f.invertInput(n);
		    } else
				stream.print(" "+n.getName());
		}
		//Output
		stream.println(" "+lutName + " #"+bestCone.getType().toString());
		
		stream.print(f.getBlifString());
		f.free();
	}

	public void printTLutBlif(Node visibleAnd, PrintStream stream, int K, String lutName) {
		Cone bestCone = visibleAnd.getBestCone(); 
		
		stream.print(".names"); 
		//LUT configuration inputs
		for (int entry=0; entry < Math.pow(2,K); entry++) {
			stream.print(" "+lutName+"_"+entry);
		}
		//Regular LUT inputs
		int j = 0;
		for (Node n:bestCone.getRegularLeaves()) {
			if (checkOutputLutInversion(n) == OutputLutInversion.AllOutsInverted)
				stream.print(" "+n.getName()+"not");
			else
				stream.print(" "+n.getName());
			j++;
		}
		//Fill up unused inputs
		while (j < K) {
			stream.print(" const0");
			j++;
		}
		//Output
		stream.println(" "+lutName + " #"+bestCone.getType().toString());

		//Implementation of LUT using mux
		for (int entry=0; entry < Math.pow(2,K); entry++) {
			//Select configuration bit
			for (int i =0; i < Math.pow(2,K); i++) {
				if (i==entry)
					stream.print('1');
				else
					stream.print('-');
			}
			//Mux
			int temp = entry;
			for (int i = 0; i < K; i++) {
				if (temp % 2 == 0)
					stream.print('0');
				else
					stream.print('1');
				temp = temp / 2;
			}
			stream.println(" 1");
		}
		stream.println();
	} 
	
	void printLatchBlif(Node latch, PrintStream stream) {
		Edge e = latch.getI0().getTail().getI0();
		Node node =  e.getTail();
		if(checkOutputLutInversion(node) == OutputLutInversion.AllOutsInverted || 
				(checkOutputLutInversion(node) == OutputLutInversion.MixedOuts && e.isInverted())) {
			stream.println(".names "+node.getName()+"not"+" "+latch.getName()+"-edge");
			stream.println("1 1");
		} else {
			stream.println(".names "+node.getName()+" "+latch.getName()+"-edge");
			if (e.isInverted()) {
				stream.println("0 1");
			} else {
				stream.println("1 1");
			}
		}
		stream.print(".latch");
		stream.print(" "+latch.getName()+"-edge");
		stream.print(" "+latch.getName());
		stream.println (" re pclk 2");
	}
	
	public double avDupl() {
		int dupCount=0;
		for (Node node:getAnds()) {
			if (node.isVisible()) {
				dupCount += node.getBestCone().getNodesInToOut().size();
			}
		}
		return (double)dupCount/this.getAnds().size();
	}

	public void fixAIG() {
		//Fix for parameters directly connected to the output
		for(Node outNode : getAllPrimaryOutputs()) {
			Edge inputEdge = outNode.getI0();
			Node inputNode = inputEdge.getTail(); 
			if(inputNode.isInput() && inputNode.isParameter()){
				String name = "a_"+outNode.getName().replace("[","_").replace("]","");
				Node andNode = addNode(name, inputNode, inputEdge.isInverted(), getConst0(), true);
				Edge e = addEdge(andNode, outNode, false);
				outNode.setI0(e); //replaces edge
				andNode.addOutput(e);
				inputNode.removeOutput(inputEdge);
				removeEdge(inputEdge);
			}
		}
		sanityCheck();
	}
	
