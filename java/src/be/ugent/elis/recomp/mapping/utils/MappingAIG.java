    private class ConfigurationEntry {
        String name;
        Node copy;
        boolean copyInv;
        ConfigurationEntry(String n, Node c0, boolean c1) {name = n; copy = c0; copyInv = c1;}
    }
    
    private class PolarisedNode {
    	private Node node;
    	private boolean inverted;
    	public PolarisedNode(PolarisedNode pnode) {
    		this(pnode.node, pnode.inverted);
    	}
    	public PolarisedNode(Node node, boolean inverted) {
			this.node = node;
			this.inverted = inverted;
		}
		public Node getNode() {
			return this.node;
		}
		public boolean isInverted() {
			return this.inverted;
		}
		public void toggleInverted(boolean toggle) {
			this.inverted ^= toggle;
		}
    }

	public AIG<Node, Edge> constructParamConfig(int K, boolean includeTLUTs, boolean includeLUTs) {
		AIG<Node, Edge> aig = new MappingAIG();
		
		
		Map<Node, PolarisedNode> parameterCopyMap = new HashMap<Node, PolarisedNode>();
		for (Node input:this.getInputs()) {
			if (input.isParameter()) {
				Node copy = aig.addNode(input.getName(), NodeType.INPUT);
				parameterCopyMap.put(input, new PolarisedNode(copy, false));
			}
		}
		
		for (Node and : getAnds()) {
			if (and.isVisible()) {
			    if(!( (includeTLUTs &&  and.getBestCone().isTuneable()) || 
			          (includeLUTs  && !and.getBestCone().isTLUT())    ))
    			          continue;
				
				Cone bestCone = and.getBestCone();
				ArrayList<Node> bestConeNodesInToOut = bestCone.getNodesInToOut();
				ArrayList<Node> regularInputs = new ArrayList<Node>(bestCone.getRegularLeaves());
				
				ArrayList<ConfigurationEntry> configuration_entries = new ArrayList<ConfigurationEntry>();
				ArrayList<ConfigurationEntry> configuration_entries_inv = new ArrayList<ConfigurationEntry>();
								
				for (int entry=0; entry < Math.pow(2, bestCone.size()); entry++) {
					Map<Node,PolarisedNode> copyMap = new HashMap<Node,PolarisedNode>(parameterCopyMap);

					int temp = entry;
					for (Node input : regularInputs) {
						copyMap.put(input, new PolarisedNode(aig.getConst0(), 
							(temp % 2 != 0) ^
							(checkOutputLutInversion(input) == OutputLutInversion.AllOutsInverted)));
						temp = temp / 2;
					}					
					
					for (Node orig: bestConeNodesInToOut) {
						Node origI0 = orig.getI0().getTail();
						PolarisedNode pnodeI0 = new PolarisedNode(copyMap.get(origI0));
						pnodeI0.toggleInverted(orig.getI0().isInverted());
						
						Node origI1 = orig.getI1().getTail();
						PolarisedNode pnodeI1 = new PolarisedNode(copyMap.get(origI1));
						pnodeI1.toggleInverted(orig.getI1().isInverted());
						
//						Constant propagation
						if (pnodeI1.getNode() == aig.getConst0()) {
							PolarisedNode swap_var = pnodeI1;
							pnodeI1 = pnodeI0;
							pnodeI0 = swap_var;
						}
						if (pnodeI0.getNode() == aig.getConst0()) {
							if (pnodeI0.isInverted()) {
								copyMap.put(orig, pnodeI1);
							} else {
								copyMap.put(orig, new PolarisedNode(aig.getConst0(), false));
							}
//						No constant propagation possible
						} else {
							Node copy = aig.findNode(pnodeI0.getNode(), pnodeI0.isInverted(), pnodeI1.getNode(), pnodeI1.isInverted());
							if (copy == null) {
								copy = aig.addNode(and.getName()+"_"+entry+"_"+orig.getName(), pnodeI0.getNode(), pnodeI0.isInverted(), pnodeI1.getNode(), pnodeI1.isInverted());
							}
							copyMap.put(orig, new PolarisedNode(copy, false));
						}
					}
							
					if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted || 
					        checkOutputLutInversion(and) == OutputLutInversion.MixedOuts) {
						PolarisedNode pnode = copyMap.get(and);
						configuration_entries_inv.add(new ConfigurationEntry(and.getName()+"not"+"_"+entry, 
																pnode.getNode(), !pnode.isInverted()));
					}
					if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted) {
						PolarisedNode pnode = copyMap.get(and);
						configuration_entries.add(new ConfigurationEntry(and.getName()+"_"+entry, 
																pnode.getNode(), pnode.isInverted()));
					}
				}
				
				configuration_entries_inv.addAll(configuration_entries);
				for(ConfigurationEntry entry : configuration_entries_inv) {
                    Node output = aig.addNode(entry.name, NodeType.OUTPUT);
                    Edge e = aig.addEdge(entry.copy, output, entry.copyInv);
                    output.setI0(e);
                    entry.copy.addOutput(e);
                }
			}
		}
		aig.sortInputsAlphanumerically();
		aig.sanityCheck();
		return aig;
	}

		
	
	
	public enum OutputLutInversion{
		notAnOutputLut, AllOutsNotInverted, AllOutsInverted, MixedOuts
	}
	
	// Determine the OutputLutInversion of an output Lut
	public OutputLutInversion checkOutputLutInversion(Node node) {
	    if(!node.isGate())
			return OutputLutInversion.notAnOutputLut;
	    
		// Determine whether it is a node and connected with an output
		boolean isOutput = false;
		Edge firstEdge = null;
		for(Edge e : node.getOutputEdges()) {
			Node head = e.getHead();
			if (head.isOutput() || head.isILatch()) {
				isOutput = true;
				firstEdge = e;
				break;
			}
		}
		if(!isOutput) {
			return OutputLutInversion.notAnOutputLut; 
		} else {
			// Determine the outputLutInversion
			OutputLutInversion outlutInv;
			if(firstEdge.isInverted())
			    outlutInv = OutputLutInversion.AllOutsInverted;
			else
			    outlutInv = OutputLutInversion.AllOutsNotInverted;
			for(Edge e : node.getOutputEdges()) {
				Node head = e.getHead();
				if (head.isOutput() || head.isILatch()) {
					if (e.isInverted() && outlutInv == OutputLutInversion.AllOutsNotInverted || 
					        (!e.isInverted() && outlutInv == OutputLutInversion.AllOutsInverted)) {
						outlutInv = OutputLutInversion.MixedOuts;
						break;
					}
				}
			}
			return outlutInv;
		}
	}
	

	public void printMappedBlif(PrintStream stream) {		
		stream.println(".model top");

		//Inputs
		stream.print(".inputs");		
		for (AbstractNode<Node, Edge> in : getInputs()) {
			stream.print(" "+in.getName());
		}	
		stream.println();
	
		//Outputs
		stream.print(".outputs");		
		for (Node out : getOutputs()) {
			stream.print(" "+out.getName());
		}
		stream.println();
		stream.println();
		
		//Constant 0
		stream.println(".names "+ getConst0().getName());
		stream.println("0");
		stream.println();
				
		//Latches
		for (Node latch : getLatches()) {
			Edge e = latch.getI0().getTail().getI0();
			
			stream.println(".names "+e.getTail().getName()+" "+latch.getName()+"-edge");
			if (e.isInverted()) {
				stream.println("0 1");
			} else {
				stream.println("1 1");
			}
			
			stream.print(".latch");
			stream.print(" "+latch.getName()+"-edge");
			stream.print(" "+latch.getName());
			stream.println (" re pclk 2");
		}
		stream.println();

		//LUTs
		for (Node and : getAnds()) {
			if (and.isVisible()) {						
				Cone bestCone = and.getBestCone();
				BooleanFunction f = bestCone.getBooleanFunction();
				
				stream.print(".names");
				//Regular LUT inputs
				Map<String, Node> map = new HashMap<String, Node>();
				for (Node n : bestCone.getAllLeaves())
					map.put(n.getName(), n);
				for(String name : f.getInputVariable()) {
					stream.print(" "+ map.get(name).getName());
				}
				//Output
				stream.println(" "+and.getName() + " #"+bestCone.getType().toString());
				
				stream.print(f.getBlifString());
				f.free();
			}
		}
		
		//Output inversion (conditional)
		//The BLIF format is a little annoying.
		for (Node out : getOutputs()) {
			Edge e = out.getI0();
			Node node = e.getTail();
			if (!node.getName().equals(out.getName())) {
				stream.println(".names "+node.getName()+" "+out.getName());
				if (e.isInverted()) {
					stream.println("0 1");
				} else {
					stream.println("1 1");
				}
			} else {
				// In some cases a latch has the same name as an output
				// this should solve that problem.
				if (e.isInverted())
		            throw new RuntimeException("Unexpected inversion of edge");
			}
		}
		
		stream.print(".end ");
		stream.flush();
	}


	public void printLutStructureBlif(PrintStream stream, int K) {
		stream.println(".model top");
		
		stream.print(".inputs");
		//Regular inputs
		for (Node in : getInputs()) {
			if (!in.isParameter()) {
				stream.print(" "+in.getName());
			}
		}
		//TLUT configuration inputs
		for (Node and : getAnds()) {
			if (and.isVisible() && and.getBestCone().isTuneable()) {
				if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted 
						|| checkOutputLutInversion(and) == OutputLutInversion.MixedOuts) {
					for (int entry=0; entry < Math.pow(2,and.getBestCone().size()); entry++) {
						stream.print(" "+and.getName()+"not"+"_"+entry);
					}
				}
				if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted) {
					for (int entry=0; entry < Math.pow(2,and.getBestCone().size()); entry++) {
						stream.print(" "+and.getName()+"_"+entry);
					}
				}
			}
		}
		stream.println();
	
		//Regular outputs
		stream.print(".outputs");		
		for (Node out : getOutputs()) {
			stream.print(" "+out.getName());
		}
		stream.println();
		stream.println();
		
		//Constant 0
		stream.println(".names "+ getConst0().getName());
		stream.println("0");
		stream.println();
				
		//Latches
		for (Node latch : getLatches()) {
			printLatchBlif(latch, stream);
		}
		stream.println();
		
		//LUTs and TLUTs
		for (Node and : getAnds()) {
			if (and.isVisible()) {
				if(and.getBestCone().isTuneable()) {
					if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted 
							|| checkOutputLutInversion(and) == OutputLutInversion.MixedOuts)
						printTLutBlif(and, stream, and.getBestCone().size(), and.getName()+"not");
					if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted)
						printTLutBlif(and, stream, and.getBestCone().size(), and.getName());
				} else {
					if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted 
							|| checkOutputLutInversion(and) == OutputLutInversion.MixedOuts)
						printLutBlif(and, stream, and.getName()+"not", true);
					if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted)
						printLutBlif(and, stream, and.getName(), false);
				}
			}
		}
		
		//Output inversion (conditional)
		//The BLIF format is a little annoying.
		for (AbstractNode<Node, Edge> out : getOutputs()) {
			Edge e = out.getI0();
			Node node = e.getTail();
			if(checkOutputLutInversion(node) == OutputLutInversion.AllOutsInverted 
					|| (checkOutputLutInversion(node) == OutputLutInversion.MixedOuts && e.isInverted())) {
				stream.println(".names "+node.getName()+"not"+" "+out.getName());
				stream.println("1 1");
			} else {
				if (!node.getName().equals(out.getName())) {
					stream.println(".names "+node.getName()+" "+out.getName());
					if (e.isInverted()) {
						stream.println("0 1");
					} else {
						stream.println("1 1");
					}
				} else {
					// In some cases a latch has the same name as an output
					// this should solve that problem.
					if (e.isInverted())
			            throw new RuntimeException("Unexpected inversion of edge");
				}
			}	
		}
		
		stream.print(".end");	
		
		stream.flush();
	}

	public void printLutBlif(Node and, PrintStream stream, String lutName, boolean inverted) {
		Cone bestCone = and.getBestCone();
		BooleanFunction f = bestCone.getBooleanFunction();
		if(inverted) {
			BooleanFunction f_old = f;
			f = f_old.invert();
			f_old.free();
		}
		
		stream.print(".names");
		//Regular LUT inputs
		Map<String, Node> map = new HashMap<String, Node>();
		for (Node n : bestCone.getAllLeaves())
			map.put(n.getName(), n);
		for(String name : f.getInputVariable()) {
			Node n = map.get(name);
			if (checkOutputLutInversion(n) == OutputLutInversion.AllOutsInverted) {
				stream.print(" "+n.getName()+"not");
				f.invertInput(n.getName());
		    } else
				stream.print(" "+n.getName());
		}
		//Output
		stream.println(" "+lutName + " #"+bestCone.getType().toString());
		
		stream.print(f.getBlifString());
		f.free();
	}

	public void printTLutBlif(Node visibleAnd, PrintStream stream, int K, String lutName) {
		Cone bestCone = visibleAnd.getBestCone(); 
		
		stream.print(".names"); 
		//LUT configuration inputs
		for (int entry=0; entry < Math.pow(2,K); entry++) {
			stream.print(" "+lutName+"_"+entry);
		}
		//Regular LUT inputs
		int j = 0;
		for (Node n:bestCone.getRegularLeaves()) {
			if (checkOutputLutInversion(n) == OutputLutInversion.AllOutsInverted)
				stream.print(" "+n.getName()+"not");
			else
				stream.print(" "+n.getName());
			j++;
		}
		//Fill up unused inputs
		while (j < K) {
			stream.print(" const0");
			j++;
		}
		//Output
		stream.println(" "+lutName + " #"+bestCone.getType().toString());

		//Implementation of LUT using mux
		for (int entry=0; entry < Math.pow(2,K); entry++) {
			//Select configuration bit
			for (int i =0; i < Math.pow(2,K); i++) {
				if (i==entry)
					stream.print('1');
				else
					stream.print('-');
			}
			//Mux
			int temp = entry;
			for (int i = 0; i < K; i++) {
				if (temp % 2 == 0)
					stream.print('0');
				else
					stream.print('1');
				temp = temp / 2;
			}
			stream.println(" 1");
		}
		stream.println();
	} 
	
	void printLatchBlif(Node latch, PrintStream stream) {
		Edge e = latch.getI0().getTail().getI0();
		Node node =  e.getTail();
		if(checkOutputLutInversion(node) == OutputLutInversion.AllOutsInverted || 
				(checkOutputLutInversion(node) == OutputLutInversion.MixedOuts && e.isInverted())) {
			stream.println(".names "+node.getName()+"not"+" "+latch.getName()+"-edge");
			stream.println("1 1");
		} else {
			stream.println(".names "+node.getName()+" "+latch.getName()+"-edge");
			if (e.isInverted()) {
				stream.println("0 1");
			} else {
				stream.println("1 1");
			}
		}
		stream.print(".latch");
		stream.print(" "+latch.getName()+"-edge");
		stream.print(" "+latch.getName());
		stream.println (" re pclk 2");
	}
	
	
	private String stripBrackets(String s) {
		return s.replace("[","").replace("]","");
	}
	
	private String replaceBrackets(String s) {
		return s.replace('[', '(').replace(']', ')');
	}
	
	private String getNodeVHDLName(Node node) {
        String nodeName;
        if(node.isConst0()) {
            nodeName = "\'0\'";
        } else if(node.isInput()) {
            nodeName = replaceBrackets(node.getName());
        } else if(isOutputLatch(node)) {
            nodeName = stripBrackets(node.getName())+"_o";
        } else {
            nodeName = stripBrackets(node.getName());
        }
        return nodeName;
    }
    
    private String getEdgeVHDLName(Edge e) {
        Node node = e.getTail();
        String nodeName = getNodeVHDLName(node);
        if(node.isConst0()) {
            if(e.isInverted())
                nodeName = "\'1\'";
        } else if(node.isInput() || node.isOLatch()) {
            if (e.isInverted())
                nodeName = "not("+nodeName+")";
        } else if(node.isGate()) {
            if(e.isInverted())
                nodeName = nodeName+"not";
        } else {
            throw new RuntimeException("Unexpected node type");
        }
        return nodeName;
	}
	
	private boolean isOutputLatch(Node latch) {
	    if(!latch.isOLatch())
	        return false;
	    String latchName = stripBrackets(latch.getName());
        for(Node outp : getOutputs()) {
            if(stripBrackets(outp.getName()).equals(latchName))
                return true;
        }
        return false;
    }
	
	public void printLutStructureVhdl(String inVhdFile, String vhdFile, String nameFile, int K) throws IOException {
		PrintStream stream = new PrintStream(new File(vhdFile));
		PrintStream nameStream = new PrintStream(new File(nameFile));
		 
		writeVhdlHeader(stream, inVhdFile, K);		
		String baseName = inVhdFile.substring(0,inVhdFile.lastIndexOf('.')).substring(inVhdFile.lastIndexOf('/')+1);
		stream.println("\nbegin");
	    
	    //all latches
	    for (Node olatch : getOLatches()) {
			printLatchVhdl(baseName, olatch, stream, K); 
	    }
	    
	    //all luts
	    for (Node and : getAnds()) {
			if (and.isVisible()) {
				if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted
				        || checkOutputLutInversion(and) == OutputLutInversion.MixedOuts) {
					printLutVhdl(baseName, and, stream, nameStream, K, and.getName()+"not", true);	 
				}
				if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted) {
					printLutVhdl(baseName, and, stream, nameStream, K, and.getName(), false);
				}
			}	
		}
		stream.println("\n");
		
        //all outputs
		for (Node out : getOutputs()) {
			Edge e = out.getI0();
			String outName = replaceBrackets(out.getName());
            stream.println(outName + " <= " + getEdgeVHDLName(e) + ";");
		}	
		stream.print("end;");
	}
	
	public void printLutVhdl(String baseName, Node visibleAnd, PrintStream stream, PrintStream nameStream, int K, String lutName, boolean inverted) {
		Cone bestCone = visibleAnd.getBestCone();
		ArrayList<Node> regularInputs = bestCone.getRegularLeavesInFixedOrder();
		int lutSize = regularInputs.size();
		String lutInstance = "\n";
/*
if K==6: 
    use the LUT6_2 primitive. This prevents two smaller LUTs from being packed together into a 6LUT with two outputs.
LUT6_2_inst : LUT6_2
generic map (INIT => X"0000000000000000") -- Specify LUT Contents
port map (
    O6 => O6,  -- 6/5-LUT output (1-bit)
    O5 => O5,  -- 5-LUT output (1-bit)
    I0 => I0,
    I1 => I1,
    I2 => I2,
    I3 => I3,
    I4 => I4,
    I5 => I5
);
*/
		if(bestCone.isTLUT()) {
		    if(K==6)
                lutInstance += baseName+"_TLUT"+lutSize+"_"+lutName+": LUT6_2" + 
                    "\ngeneric map (\n\tINIT =>X\"1\")\nport map (\n\tO6 => " + lutName +
                    ",\n\tO5 => open";
            else
                lutInstance += baseName+"_TLUT"+lutSize+"_"+lutName+": LUT"+lutSize +
                    "\ngeneric map (\n\tINIT =>X\"1\")\nport map (\n\tO => " + lutName;
			nameStream.println(baseName+"_TLUT"+lutSize+"_"+lutName);
		} else {
			BooleanFunction expr = bestCone.getBooleanFunction();
			if(inverted)
			    expr = expr.invert();
			//check for inversion at the inputs, change Boolean Function if necessary
			for (Node inputNode : regularInputs){
				if(checkOutputLutInversion(inputNode)==OutputLutInversion.AllOutsInverted) {
					expr.invertInput(inputNode.getName());
				}
			}
			lutInstance += baseName+"_LUT"+lutSize+"_"+lutName+": LUT"+lutSize + 
			    "\ngeneric map (\n\tINIT =>"+expr.getVHDLString()+")\nport map (\n\tO => "+lutName;
		}
		for (int i = 0; i < lutSize ; i++) {
		    Node input = regularInputs.get(i);
		    String nodeName = getNodeVHDLName(input);
			if(checkOutputLutInversion(input) == OutputLutInversion.AllOutsInverted)
			    nodeName += "not";
			lutInstance += ",\n\tI" + Integer.toString(i) + " => " + nodeName;
		}
		if(K==6 && bestCone.isTLUT()) {
		    for (int i = lutSize; i < K ; i++)
		        lutInstance += ",\n\tI" + Integer.toString(i) + " => '0'";
		}
		lutInstance += ");\n";
		
		stream.print(lutInstance);
	} 
	
	
	public void printLatchVhdl(String baseName, Node olatch, PrintStream stream, int K) {
	    assert(olatch.isOLatch());
	    Node latch = olatch.getI0().getTail();
	    assert(latch.isLatch());
		String latchInstance;
		String latchName = getNodeVHDLName(olatch);
		latchInstance = "\n" + baseName + "_FD_" + latchName + 
		    ": FD\ngeneric map (\n\tINIT =>\'0\')\nport map (Q => " + latchName;
		latchInstance += ",\n\tC => clk";
				
		Edge e = latch.getI0().getTail().getI0();
		
		latchInstance += ",\n\tD => " + getEdgeVHDLName(e);	
		latchInstance += ");\n";
		
		stream.print(latchInstance);
	} 
	
	
	
	private void writeVhdlHeader(PrintStream stream, String inVhdFile, int K) throws IOException {
		// Read header old vhdl file as a String
		String baseName = inVhdFile.substring(0,inVhdFile.lastIndexOf('.')).substring(inVhdFile.lastIndexOf('/')+1);
	    BufferedReader vhdlFileReader = new BufferedReader( new FileReader(new File(inVhdFile)));
	    String vhdlFileLine = vhdlFileReader.readLine();
	    String header = "--WARNING: Don't edit. Automatically regenerated file (TLUT flow)\n";
	    while(vhdlFileLine.toLowerCase().indexOf("architecture") == -1){
	        header = header + vhdlFileLine + '\n' ;
	        vhdlFileLine = vhdlFileReader.readLine();    
	    }
	    
	    String [] headerArray;
	    
	    // Insert unisim library declaration if necessary
	    headerArray = header.split("[e|E][n|N][t|T][i|I][t|T][y|Y]");
	    String libraryString = "-- synopsys translate_off\nlibrary UNISIM;\nuse unisim.Vcomponents.all;\n-- synopsys translate_on\n";
	    if (header.indexOf("library UNISIM;")== -1)
	        header = headerArray[0] + libraryString + "\nentity" + headerArray[1];

	    stream.println(header.trim());
	    
	    stream.println("\n"+vhdlFileLine);
	    
	    // Add LUT templates 
	    if(K==6) {
	        stream.println("component LUT6_2\ngeneric (INIT : bit_vector := X\"1\");\n" +
                "port (\n\tO6 : out STD_ULOGIC;\n\tO5 : out STD_ULOGIC;\n\tI0 : in STD_ULOGIC;" + 
                "\n\tI1 : in STD_ULOGIC;\n\tI2 : in STD_ULOGIC;\n\tI3 : in STD_ULOGIC;" +
                "\n\tI4 : in STD_ULOGIC;\n\tI5 : in STD_ULOGIC);\nend component;\n");
	    }
        for (int i = 1;i<=K;i++) {	
            String lutComponent = "component LUT" + Integer.toString(i) + 
                "\ngeneric (INIT : bit_vector := X\"1\");\nport   (O : out STD_ULOGIC";
            for(int j = 0;j<i;j++)
                lutComponent = lutComponent + "; \n\tI"+Integer.toString(j)+": in STD_ULOGIC";
            lutComponent = lutComponent + ");\nend component;\n";
            stream.println(lutComponent);
        }
	    // Add FF template 
	    String ffComponent = "component FD\ngeneric (INIT : bit:= '1');\nport   (Q : out STD_ULOGIC;\n\tC : in STD_ULOGIC;\n\tD : in STD_ULOGIC);\nend component;\n";
	    stream.println(ffComponent);
	    
	    // Add declaration of signals and init attributes
	    String signalDeclarations = "";
		String initAttributes = "\nattribute INIT : string;";
		String sAttributes = "\nattribute S : string;";

		
	    for (Node and : getAnds()) {
			if (and.isVisible()) {	
				Cone bestCone = and.getBestCone();
				ArrayList<Node> regularInputs = bestCone.getRegularLeavesInFixedOrder();
				int lutSize = regularInputs.size();
				String lutOrTlut = bestCone.isTLUT() ? "TLUT" : "LUT";
				
				if(checkOutputLutInversion(and) == OutputLutInversion.AllOutsInverted || 
				        checkOutputLutInversion(and) == OutputLutInversion.MixedOuts) {
					signalDeclarations = signalDeclarations + "\nsignal "+and.getName()+"not : STD_ULOGIC ;";
					initAttributes += "\nattribute INIT of "+baseName+"_"+lutOrTlut+lutSize+"_"+and.getName()+
					    "not: label is \""+Integer.toString((int) java.lang.Math.pow(2, lutSize))+"\";";
                    sAttributes += "\nattribute S of "+and.getName()+"not : signal is \"YES\";";
					    
				}
				if(checkOutputLutInversion(and) != OutputLutInversion.AllOutsInverted){
					signalDeclarations = signalDeclarations + "\nsignal "+and.getName()+" : STD_ULOGIC ;";
					initAttributes += "\nattribute INIT of "+baseName+"_"+lutOrTlut+lutSize+"_"+and.getName()+
					    ": label is \""+Integer.toString((int) java.lang.Math.pow(2, lutSize))+"\";";
                    sAttributes += "\nattribute S of "+and.getName()+" : signal is \"YES\";";
					    
				}
					
			}	
		}
	    
	    for (Node latch : getOLatches()) {
	    	String nameSuffix = isOutputLatch(latch) ? "_o" : "";
			
			signalDeclarations +=  "\nsignal " + stripBrackets(latch.getName()) +
			    nameSuffix + " : STD_ULOGIC ;";
			initAttributes += "\nattribute INIT of " + baseName + "_FD_" + stripBrackets(latch.getName()) +
			    nameSuffix + " : label is \"0\";";
            sAttributes += "\nattribute S of "+stripBrackets(latch.getName()) + nameSuffix + " : signal is \"YES\";";
				
			//signalDeclarations = signalDeclarations + "\nsignal "+stripBrackets(latch.getName()) +" : STD_ULOGIC ;";
			//initAttributes = initAttributes + "\nattribute INIT of FD_"+stripBrackets(latch.getName())+" : label is \"0\";"; 
				
		}
	    
	    
	    stream.println(signalDeclarations);
	    stream.println(initAttributes);
        stream.println(sAttributes);
		
	    // Add declaration of lock attributes
		String lockAttributes = "\nattribute lock_pins : string;";
		for (int i = 1;i<=K;i++) {	
	    	lockAttributes = lockAttributes + "\nattribute lock_pins of LUT"+Integer.toString(i)+": component is \"ALL\";";
	    }
	    if(K==6)
	    	lockAttributes = lockAttributes + "\nattribute lock_pins of LUT6_2: component is \"ALL\";";

		stream.println(lockAttributes);
	}

	public double avDupl() {
		int dupCount=0;
		for (Node node:getAnds()) {
			if (node.isVisible()) {
				dupCount += node.getBestCone().getNodesInToOut().size();
			}
		}
		return (double)dupCount/this.getAnds().size();
	}

	public void fixAIG() {
		//Fix for parameters directly connected to the output
		for(Node outNode : getAllPrimaryOutputs()) {
			Edge inputEdge = outNode.getI0();
			Node inputNode = inputEdge.getTail(); 
			if(inputNode.isInput() && inputNode.isParameter()){
				String name = "a_"+outNode.getName().replace("[","_").replace("]","");
				Node andNode = addNode(name, inputNode, inputEdge.isInverted(), getConst0(), true);
				Edge e = addEdge(andNode, outNode, false);
				outNode.setI0(e); //replaces edge
				andNode.addOutput(e);
				inputNode.removeOutput(inputEdge);
				removeEdge(inputEdge);
			}
		}
		sanityCheck();
	}
	
