	public double getDepth() {
		Vector<Node> PO = new Vector<Node>();
		PO.addAll(getOutputs());
		PO.addAll(getILatches());
		
		double result = 0;
		for (Node node : PO) {
			if (node.getDepth() > result) {
				result = node.getDepth();
			}
		}
		
		return result;
	}
	
