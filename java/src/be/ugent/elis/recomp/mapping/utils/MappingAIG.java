	public Collection<Cone> getVisibleCones() {
		ArrayList<Cone> result = new ArrayList<Cone>();
		for (Node node:getAnds())
			if (node.isVisible())
				result.add(node.getBestCone());
		return result;
	}

	public int numLUTResourcesUsed() {
		int result = 0;
		for (Cone cone : getVisibleCones())
				if (cone.usesLUTResource())
					result++;
		return result;
	}
	
	public int numTLUTResourcesUsed() {
		int result = 0;
		for (Cone cone : getVisibleCones())
				if (cone.usesTLUTResource())
					result++;
		return result;
	}
	
	public int numTCONResourcesUsed() {
		int result = 0;
		for (Cone cone : getVisibleCones())
				if (cone.usesTCONResource())
					result++;
		return result;
	}

	public double getDepth() {
		Vector<Node> PO = new Vector<Node>();
		PO.addAll(getOutputs());
		PO.addAll(getILatches());
		
		double result = 0;
		for (Node node : PO) {
			if (node.getDepth() > result) {
				result = node.getDepth();
			}
		}
		
		return result;
	}
	
